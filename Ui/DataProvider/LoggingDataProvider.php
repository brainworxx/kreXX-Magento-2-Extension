namespace Brainworxx\M2krexx\Ui\DataProvider;

use Magento\Ui\DataProvider\AbstractDataProvider;
use Brainworxx\M2krexx\Model\Logging\CollectionFactory;
use Magento\Framework\App\ObjectManager;

/**
 * Class ProductDataProvider
 */
class LoggingDataProvider extends AbstractDataProvider
{
    /**
     * foobar collection
     *
     * @var \Brainworxx\M2krexx\Model\Logging\CollectionFactory
     */
    protected $collection;

    /**
     * @var \Magento\Ui\DataProvider\AddFieldToCollectionInterface[]
     */
    protected $addFieldStrategies;

    /**
     * @var \Magento\Ui\DataProvider\AddFilterToCollectionInterface[]
     */
    protected $addFilterStrategies;

    /**
     * Construct
     *
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param array $meta
     * @param array $data
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        array $meta = [],
        array $data = []
    ) {
        parent::__construct($name, $primaryFieldName, $requestFieldName, $meta, $data);
        $this->collection = ObjectManager::getInstance()->get(CollectionFactory::class)->create();
    }

    /**
     * Get collection
     *
     * @return \Brainworxx\M2krexx\Model\Logging\CollectionFactory
     */
    public function getCollection()
    {
        return $this->collection;

    }

    /**
     * Get data
     *
     * @return array
     */
    public function getData()
    {
        if (!$this->getCollection()->isLoaded()) {
            $this->getCollection()->load();
        }
        return $this->getCollection()->toArray();
    }
}